package javaBasics;

public class DataTypes {
	/*
	 Data Types
	 1. Primitive data types
	 2.Non - primitive data types
	 
	 1. Primitive data types - also called scaler data types - these can hold a single value at a time
	 2.Non - primitive data types- these can hold multiple values at a time ex- Array, String, Matrix
	 
	 1. Primitive data types-
	    1. Boolean- true 1 / false 0  - 1byte
	    2. byte-this holds integer values of size 1 byte
	    3. short- 2 byte it can hold integer numbers
	    4. int- 4 bytes- it can hold integer numbers 
	    5. long- 8bytes - it can hold integer numbers 
	    6. char- 2 byte, number, symbols,chars one at a time.
	    7. float- 4 byte - it can hold decimal points numbers. 
	    8  double- 8 byte- it can hold decimal points numbers
	    
	    
	 
	 
	 1byte= 8bit(binary 0,1), 1000byte= 1kb, 1000kb=1mb
	 
	 
	 
	 */
	
	
	public static void main(String[] args) {
		int a,b;
		a=20;
		b=50;
		int c=a+b;
		System.out.println("Addition of a & b is:"+c);
		
		byte x,y;
		x=10;
		y=20;
		int z=x+y;
		
		System.out.println("Addition of x & y is:"+z);
		
	}

}
