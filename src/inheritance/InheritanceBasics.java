package inheritance;

public class InheritanceBasics {
	/*
	 
	 Inheritance:- 
	      One class(Object) acquires the properties / behaviors of another class(Object).
	   
	   Parent class- Super class
	   child class/ sub class
	   
	   - it reduces the code size.
	   - extends keyword
	   
	   Types
	   1. Single 
	   2. multilevel
	   3. hierarchical
	   4. multiple - does not supported by java
	   5. hybrid
	   
	   
	     1. Single 
	     
	         A -->B
	         
	     2. multilevel
	           A-->B--->C
	     
	     3.hierarchical
	         A-->B,   A--->C
	         
	     4. multiple
	        A--->B,    C---->B
	        
	     5. hybrid
	         it is combination of hierarchical and multiple inheritance.
	     
	     
	     Class1- Employee- Salary()
	     Class2- Programmer,Hr, SupportExecutive;
	     
	        
	 
	 */

}
